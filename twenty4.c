byte revsbox[256];

#define K_HASH_ROUNDS 32
#define S_BOX_ROUNDS  17


byte rot8left(byte in, int rot) {
  return (in >> (8-rot)) | (in << rot);
}

byte rot8right(byte in, int rot) {
  return (in << (8-rot)) | (in >> rot);
}

